let hero = 'Superman';
switch (hero) {
    case 'Superman':
        console.log('Super Strength');
        console.log('X-Ray Vision');
        break;
    case 'Batman':
        console.log('Intelligence');
        console.log('Martial Arts');
        break;
    default:
        console.log('Justice League');
        break;
}



